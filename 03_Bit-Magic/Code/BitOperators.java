public class BitOperators {
    public static void main(String[] args) {
        int x = 3;
        int y = 6;
        System.out.println(x&y);// Bitwise AND(&)
        System.out.println(x|y);// Bitwise OR(|)
        System.out.println(x^y);// Bitwise OR(^)
        System.out.println(~x);// Bitwise NOT(~) -> take single operand
                                
    }
}
